# 년주 계산
# ============================================
def calc_년주(year, month, day):
    """
    년주 계산 - 입춘 기준
    입춘 전이면 전년도로 계산
    입춘: 대략 2월 4일 (정확한 계산은 별도 필요)
    """
    # 입춘 전이면 전년도
    if month == 1 or (month == 2 and day < 4):
        year = year - 1
    
    # 1984년 = 갑자년 (index 0)
    index = (year - 1984) % 60
    if index < 0:
        index += 60
    
    천간_idx = index % 10
    지지_idx = index % 12
    
    return 천간[천간_idx], 지지[지지_idx]

