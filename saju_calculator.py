def calc_십성(일간, 대상, is_천간=True):
    """십성 계산"""
    일간_idx = 천간.index(일간)
    일간_오행 = 천간_오행[일간_idx]
    일간_음양 = 천간_음양[일간_idx]
    
    if is_천간:
        대상_idx = 천간.index(대상)
        대상_오행 = 천간_오행[대상_idx]
        대상_음양 = 천간_음양[대상_idx]
    else:
        # 지지는 지장간 본기 기준으로 계산
        본기 = 지장간_본기[대상]
        본기_idx = 천간.index(본기)
        대상_오행 = 천간_오행[본기_idx]
        대상_음양 = 천간_음양[본기_idx]
    
    관계 = 오행_관계[일간_오행][대상_오행]
    
    # 같은 음양이면 [0], 다른 음양이면 [1]
    if 일간_음양 == 대상_음양:
        return 관계[0]
    else:
        return 관계[1]

