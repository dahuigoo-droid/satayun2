from datetime import date

def calc_일주(year, month, day):
    """
    일주 계산 - 기준일로부터 날짜 차이
    기준일: 1900년 1월 1일 = 갑진일 (index 40)
    """
    기준일 = date(1900, 1, 1)
    기준일_갑자_index = 10  # 갑술 (역산으로 확인)
    
    대상일 = date(year, month, day)
    차이 = (대상일 - 기준일).days
    
    index = (기준일_갑자_index + 차이) % 60
    
    천간_idx = index % 10
    지지_idx = index % 12
    
    return 천간[천간_idx], 지지[지지_idx]

