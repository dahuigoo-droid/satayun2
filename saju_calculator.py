def get_월_from_양력(month, day):
    """양력 월일로 절기 기준 월 계산 (인월=1, 묘월=2, ...)"""
    for 월, (시작월, 시작일) in 절기_시작.items():
        다음월 = (월 % 12) + 1
        다음_시작월, 다음_시작일 = 절기_시작[다음월]
        
        if 시작월 == month:
            if day >= 시작일:
                return 월
        
    # 간단한 매핑 (대략적)
    if month == 2 and day >= 4:
        return 1  # 인월
    elif month == 3 and day >= 6:
        return 2  # 묘월
    elif month == 4 and day >= 5:
        return 3  # 진월
    elif month == 5 and day >= 6:
        return 4  # 사월
    elif month == 6 and day >= 6:
        return 5  # 오월
    elif month == 7 and day >= 7:
        return 6  # 미월
    elif month == 8 and day >= 8:
        return 7  # 신월
    elif month == 9 and day >= 8:
        return 8  # 유월
    elif month == 10 and day >= 8:
        return 9  # 술월
    elif month == 11 and day >= 7:
        return 10 # 해월
    elif month == 12 and day >= 7:
        return 11 # 자월
    elif month == 1 and day >= 6:
        return 12 # 축월
    else:
        # 이전 월
        if month == 1:
            return 11  # 자월
        elif month == 2:
            return 12  # 축월
        elif month == 3:
            return 1   # 인월
        elif month == 4:
            return 2   # 묘월
        elif month == 5:
            return 3   # 진월
        elif month == 6:
            return 4   # 사월
        elif month == 7:
            return 5   # 오월
        elif month == 8:
            return 6   # 미월
        elif month == 9:
            return 7   # 신월
        elif month == 10:
            return 8   # 유월
        elif month == 11:
            return 9   # 술월
        elif month == 12:
            return 10  # 해월

def calc_월주(년간, month, day):
    """월주 계산"""
    월_index = get_월_from_양력(month, day)  # 1=인월, 2=묘월, ...
    
    # 월지 계산 (인월=인(2), 묘월=묘(3), ...)
    지지_idx = (월_index + 1) % 12  # 인=2
    
    # 월간 계산 (오호둔갑)
    시작_천간 = 월주_천간_시작[년간]
    천간_idx = (시작_천간 + 월_index - 1) % 10
    
    return 천간[천간_idx], 지지[지지_idx]

