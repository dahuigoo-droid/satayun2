def calc_오행_분석(사주_결과):
    """오행 개수 분석"""
    오행_count = {'목': 0, '화': 0, '토': 0, '금': 0, '수': 0}
    
    for 주 in ['년주', '월주', '일주', '시주']:
        간, 지 = 사주_결과[주]
        간_idx = 천간.index(간)
        지_idx = 지지.index(지)
        오행_count[천간_오행[간_idx]] += 1
        오행_count[지지_오행[지_idx]] += 1
    
    return 오행_count

