    사주 = calc_사주(year, month, day, hour, minute)
    
    년간 = 사주['년주'][0]
    월간 = 사주['월주'][0]
    월지 = 사주['월주'][1]
    일간 = 사주['일주'][0]
    
    년간_idx = 천간.index(년간)
    년간_음양 = 천간_음양[년간_idx]
    
    # 순행/역행 결정
    # 남자 + 양간 = 순행, 남자 + 음간 = 역행
    # 여자 + 양간 = 역행, 여자 + 음간 = 순행
    if gender == '남':
        순행 = (년간_음양 == '양')
    else:
        순행 = (년간_음양 == '음')
    
    # 대운수 계산 (간략화 - 실제로는 절기까지 일수로 계산)
    # 여기서는 생일 기준 대략적 계산
    절기_기준일 = {
        1: 6, 2: 4, 3: 6, 4: 5, 5: 6, 6: 6,
        7: 7, 8: 8, 9: 8, 10: 8, 11: 7, 12: 7
    }
    
    절기일 = 절기_기준일[month]
    
    if 순행:
        # 다음 절기까지 일수
        if day < 절기일:
            일수 = 절기일 - day
        else:
            # 다음 달 절기까지
            다음월 = (month % 12) + 1
            일수 = (30 - day) + 절기_기준일[다음월]
    else:
        # 이전 절기까지 일수
        if day >= 절기일:
            일수 = day - 절기일
        else:
            # 이전 달 절기까지
            이전월 = month - 1 if month > 1 else 12
            일수 = day + (30 - 절기_기준일[이전월])
    
    대운수 = round(일수 / 3)
    if 대운수 == 0:
        대운수 = 1
    
    # 대운 목록 생성 (10개)
    월간_idx = 천간.index(월간)
    월지_idx = 지지.index(월지)
    
    대운_list = []
    for i in range(10):
        if 순행:
            천간_idx = (월간_idx + i + 1) % 10
            지지_idx = (월지_idx + i + 1) % 12
        else:
            천간_idx = (월간_idx - i - 1) % 10
            지지_idx = (월지_idx - i - 1) % 12
        
        대운_천간 = 천간[천간_idx]
        대운_지지 = 지지[지지_idx]
        
        # 나이 계산
        시작_나이 = 대운수 + (i * 10)
        
        # 천간 십성
        천간_십성 = calc_십성(일간, 대운_천간, True)
        
        # 지지 십성 (지장간 본기 기준)
        지지_십성 = calc_십성(일간, 대운_지지, False)
        
        # 12운성
        운성 = calc_12운성(일간, 대운_지지)
        
        # 12신살
        년지 = 사주['년주'][1]
        신살 = calc_12신살(년지, 대운_지지)
        
        대운_list.append({
            '나이': 시작_나이,
            '천간': 대운_천간,
            '지지': 대운_지지,
            '천간_십성': 천간_십성,
            '지지_십성': 지지_십성,
            '12운성': 운성,
            '12신살': 신살,
        })
    
    return {
        '대운수': 대운수,
        '순행': 순행,
        '대운': 대운_list,
        '사주': 사주,
    }

